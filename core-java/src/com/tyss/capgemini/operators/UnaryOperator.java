package com.tyss.capgemini.operators;

public class UnaryOperator {
	public static void main(String[] args) {
		int j=10;
		int i=10;
		boolean isTrue=true;
		 
		
		System.out.println(j);
		System.out.println(!isTrue);
		System.out.println(++i);
		System.out.println(i);
		System.out.println(i++);
		System.out.println(i);
		System.out.println("Value of i with preicrement: ++i");
		System.out.println("value of i: " +i);	
		System.out.println("Value of j with postincrement:+ j++");
		System.out.println("value of j:" +j);	
		System.out.println("Value of i with predecrement:+ --i");
		System.out.println("value of i: " +i);	
		System.out.println("Value of j with postdecrement:+j--");
		System.out.println("value of j: " +j);	
	}

}
